    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        if (evt.getClickCount() == 2) {
            List<CauHoi> listCauHoi = null;
            BeginTestDialog beginTest = null;
            switch (jList1.getSelectedValue()) {
                case "Math":
                    listCauHoi = DbController.getListQuestionOfMathTest(listSubjectExam.get(jList1.getSelectedValue()));
                    beginTest = new BeginTestDialog(null, true, listCauHoi);
                    beginTest.getContentPane();
                    beginTest.setVisible(true);
                    if (beginTest.getFinished()) {
                        dlm.removeElement("Math");
                        jList1.setModel(dlm);
                    }
                    break;
                case "Literature":
                    listCauHoi = DbController.getListQuestionOfLiteratureTest(listSubjectExam.get(jList1.getSelectedValue()));
                    beginTest = new BeginTestDialog(null, true, listCauHoi);
                    beginTest.getContentPane();
                    beginTest.setVisible(true);
                    if (beginTest.getFinished()) {
                        dlm.removeElement("Literature");
                        jList1.setModel(dlm);
                    }
                    break;
                case "History":
                    listCauHoi = DbController.getListQuestionOfHistoryTest(listSubjectExam.get(jList1.getSelectedValue()));
                    beginTest = new BeginTestDialog(null, true, listCauHoi);
                    beginTest.getContentPane();
                    beginTest.setVisible(true);
                    if (beginTest.getFinished()) {
                        dlm.removeElement("History");
                        jList1.setModel(dlm);
                    }
                    break;
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

